package com.breeze.database.config;

import com.baomidou.mybatisplus.core.injector.AbstractMethod;
import com.baomidou.mybatisplus.core.injector.DefaultSqlInjector;
import com.baomidou.mybatisplus.core.metadata.TableInfo;
import com.breeze.database.method.BreezeInsertAllBatch;

import java.util.List;

/**
 * 微风 sql注入器
 *
 * @author gaoweixuan
 * @date 2022-10-12
 */
public class BreezeLogicSqlInjector extends DefaultSqlInjector {

    /**
     * 如果只需增加方法，保留MP自带方法
     * 可以super.getMethodList() 再add
     *
     * @param mapperClass mapper类
     * @param tableInfo   表信息
     * @return {@link List}<{@link AbstractMethod}>
     */
    @Override
    public List<AbstractMethod> getMethodList(Class<?> mapperClass, TableInfo tableInfo) {
        List<AbstractMethod> methodList = super.getMethodList(mapperClass, tableInfo);
        methodList.add(new BreezeInsertAllBatch("insertAllBatch"));
        return methodList;
    }

}
